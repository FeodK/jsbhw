function sum(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a >= b ? a - b : b - a;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

console.log(sum(2, 6));
console.log(subtract(2, 6));
console.log(multiply(4, 5));
console.log(divide(10, 2));
